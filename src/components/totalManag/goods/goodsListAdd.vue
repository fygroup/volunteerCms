<script>
import UE from "@/components/common/ue";
export default {
  	components: { UE },
  	data() {
		return {
            dialogImageUrl: '',
            dialogVisible: false,
			defaultMsg: '',
            config: {
                initialFrameWidth: null,
                initialFrameHeight: 450
            },
            ue1: "ue1", // 不同编辑器必须不同的id
